package tun

import (
	"testing"
)

func TestValidatePacket(t *testing.T) {
	tests := []struct {
		name    string
		packet  []byte
		want    bool
		comment string
	}{
		{
			name:    "empty packet",
			packet:  []byte{},
			want:    false,
			comment: "packet too short",
		},
		{
			name:    "too short for IP header",
			packet:  []byte{0x45, 0x00, 0x00},
			want:    false,
			comment: "less than 20 bytes",
		},
		{
			name: "valid IPv4 minimal",
			packet: []byte{
				0x45, 0x00, 0x00, 0x14, // Version=4, IHL=5, TOS=0, Total Length=20
				0x00, 0x00, 0x00, 0x00, // ID, Flags, Fragment Offset
				0x40, 0x11, 0x00, 0x00, // TTL=64, Protocol=UDP, Checksum
				0xc0, 0xa8, 0x01, 0x01, // Source IP: 192.168.1.1
				0xc0, 0xa8, 0x01, 0x02, // Dest IP: 192.168.1.2
			},
			want:    true,
			comment: "minimal valid IPv4 packet",
		},
		{
			name: "valid IPv4 with data",
			packet: []byte{
				0x45, 0x00, 0x00, 0x28, // Version=4, IHL=5, TOS=0, Total Length=40
				0x12, 0x34, 0x00, 0x00, // ID, Flags, Fragment Offset
				0x40, 0x06, 0x00, 0x00, // TTL=64, Protocol=TCP, Checksum
				0x0a, 0x00, 0x00, 0x01, // Source IP: 10.0.0.1
				0x0a, 0x00, 0x00, 0x02, // Dest IP: 10.0.0.2
				// 20 bytes of TCP data
				0x00, 0x50, 0x1f, 0x90, 0x00, 0x00, 0x00, 0x00,
				0x00, 0x00, 0x00, 0x00, 0x50, 0x02, 0x20, 0x00,
				0x00, 0x00, 0x00, 0x00,
			},
			want:    true,
			comment: "IPv4 with TCP payload",
		},
		{
			name: "valid IPv6 minimal",
			packet: []byte{
				0x60, 0x00, 0x00, 0x00, // Version=6, Traffic Class, Flow Label
				0x00, 0x00, 0x11, 0x40, // Payload Length=0, Next Header=UDP, Hop Limit=64
				// Source address (16 bytes)
				0x20, 0x01, 0x0d, 0xb8, 0x00, 0x00, 0x00, 0x00,
				0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x01,
				// Destination address (16 bytes)
				0x20, 0x01, 0x0d, 0xb8, 0x00, 0x00, 0x00, 0x00,
				0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x02,
			},
			want:    true,
			comment: "minimal valid IPv6 packet",
		},
		{
			name: "valid IPv6 with data",
			packet: []byte{
				0x60, 0x00, 0x00, 0x00, // Version=6, Traffic Class, Flow Label
				0x00, 0x08, 0x11, 0x40, // Payload Length=8, Next Header=UDP, Hop Limit=64
				// Source address (16 bytes)
				0xfe, 0x80, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
				0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x01,
				// Destination address (16 bytes)
				0xfe, 0x80, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
				0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x02,
				// UDP header (8 bytes)
				0x00, 0x35, 0x00, 0x35, 0x00, 0x08, 0x00, 0x00,
			},
			want:    true,
			comment: "IPv6 with UDP header",
		},
		{
			name: "invalid IP version 0",
			packet: []byte{
				0x00, 0x00, 0x00, 0x14, // Version=0 (invalid)
				0x00, 0x00, 0x00, 0x00,
				0x40, 0x11, 0x00, 0x00,
				0xc0, 0xa8, 0x01, 0x01,
				0xc0, 0xa8, 0x01, 0x02,
			},
			want:    false,
			comment: "version 0 is invalid",
		},
		{
			name: "invalid IP version 3",
			packet: []byte{
				0x30, 0x00, 0x00, 0x14, // Version=3 (invalid)
				0x00, 0x00, 0x00, 0x00,
				0x40, 0x11, 0x00, 0x00,
				0xc0, 0xa8, 0x01, 0x01,
				0xc0, 0xa8, 0x01, 0x02,
			},
			want:    false,
			comment: "version 3 is invalid",
		},
		{
			name: "invalid IP version 7",
			packet: []byte{
				0x70, 0x00, 0x00, 0x14, // Version=7 (invalid)
				0x00, 0x00, 0x00, 0x00,
				0x40, 0x11, 0x00, 0x00,
				0xc0, 0xa8, 0x01, 0x01,
				0xc0, 0xa8, 0x01, 0x02,
			},
			want:    false,
			comment: "version 7 is invalid",
		},
		{
			name: "IPv4 IHL too small",
			packet: []byte{
				0x44, 0x00, 0x00, 0x14, // Version=4, IHL=4 (too small, min is 5)
				0x00, 0x00, 0x00, 0x00,
				0x40, 0x11, 0x00, 0x00,
				0xc0, 0xa8, 0x01, 0x01,
				0xc0, 0xa8, 0x01, 0x02,
			},
			want:    false,
			comment: "IHL must be at least 5",
		},
		{
			name: "IPv4 packet shorter than IHL",
			packet: []byte{
				0x46, 0x00, 0x00, 0x14, // Version=4, IHL=6 (24 bytes)
				0x00, 0x00, 0x00, 0x00,
				0x40, 0x11, 0x00, 0x00,
				0xc0, 0xa8, 0x01, 0x01,
				0xc0, 0xa8, 0x01, 0x02,
				// Only 20 bytes total, but IHL says 24
			},
			want:    false,
			comment: "packet shorter than IHL indicates",
		},
		{
			name: "IPv6 too short",
			packet: []byte{
				0x60, 0x00, 0x00, 0x00, // Version=6
				0x00, 0x00, 0x11, 0x40,
				// Only 8 bytes, need 40 for IPv6
			},
			want:    false,
			comment: "IPv6 header must be 40 bytes",
		},
		{
			name: "IPv4 total length mismatch (too large)",
			packet: []byte{
				0x45, 0x00, 0x00, 0x64, // Version=4, IHL=5, Total Length=100
				0x00, 0x00, 0x00, 0x00,
				0x40, 0x11, 0x00, 0x00,
				0xc0, 0xa8, 0x01, 0x01,
				0xc0, 0xa8, 0x01, 0x02,
				// Only 20 bytes, but total length says 100
			},
			want:    false,
			comment: "total length exceeds packet size",
		},
		{
			name: "IPv4 total length too small",
			packet: []byte{
				0x45, 0x00, 0x00, 0x10, // Version=4, IHL=5, Total Length=16 (less than header)
				0x00, 0x00, 0x00, 0x00,
				0x40, 0x11, 0x00, 0x00,
				0xc0, 0xa8, 0x01, 0x01,
				0xc0, 0xa8, 0x01, 0x02,
			},
			want:    false,
			comment: "total length less than IHL",
		},
		{
			name: "IPv6 payload length mismatch",
			packet: []byte{
				0x60, 0x00, 0x00, 0x00, // Version=6
				0x00, 0x64, 0x11, 0x40, // Payload Length=100 (but no payload)
				// Source address (16 bytes)
				0x20, 0x01, 0x0d, 0xb8, 0x00, 0x00, 0x00, 0x00,
				0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x01,
				// Destination address (16 bytes)
				0x20, 0x01, 0x0d, 0xb8, 0x00, 0x00, 0x00, 0x00,
				0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x02,
				// Only 40 bytes total, but payload length says 100
			},
			want:    false,
			comment: "payload length exceeds available data",
		},
		{
			name:    "single byte",
			packet:  []byte{0x45},
			want:    false,
			comment: "way too short",
		},
		{
			name: "IPv4 with options",
			packet: []byte{
				0x46, 0x00, 0x00, 0x18, // Version=4, IHL=6 (24 bytes), Total Length=24
				0x00, 0x00, 0x00, 0x00,
				0x40, 0x11, 0x00, 0x00,
				0xc0, 0xa8, 0x01, 0x01,
				0xc0, 0xa8, 0x01, 0x02,
				// 4 bytes of options
				0x00, 0x00, 0x00, 0x00,
			},
			want:    true,
			comment: "IPv4 with valid options",
		},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			got := ValidatePacket(tt.packet)
			if got != tt.want {
				t.Errorf("ValidatePacket() = %v, want %v (%s)", got, tt.want, tt.comment)
			}
		})
	}
}

func TestValidatePacket_Benchmarks(t *testing.T) {
	// Create realistic packets for performance testing
	ipv4Packet := []byte{
		0x45, 0x00, 0x00, 0x54, // Version=4, IHL=5, Total Length=84
		0x12, 0x34, 0x40, 0x00, // ID, Flags, Fragment Offset
		0x40, 0x01, 0x00, 0x00, // TTL=64, Protocol=ICMP, Checksum
		0xc0, 0xa8, 0x01, 0x01, // Source IP
		0x08, 0x08, 0x08, 0x08, // Dest IP
	}
	// Add 64 bytes of payload
	ipv4Packet = append(ipv4Packet, make([]byte, 64)...)

	ipv6Packet := []byte{
		0x60, 0x00, 0x00, 0x00, // Version=6
		0x00, 0x40, 0x11, 0x40, // Payload Length=64, Next Header=UDP
		// Source address
		0x20, 0x01, 0x0d, 0xb8, 0x00, 0x00, 0x00, 0x00,
		0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x01,
		// Destination address
		0x20, 0x01, 0x0d, 0xb8, 0x00, 0x00, 0x00, 0x00,
		0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x02,
	}
	// Add 64 bytes of payload
	ipv6Packet = append(ipv6Packet, make([]byte, 64)...)

	// Verify packets are valid
	if !ValidatePacket(ipv4Packet) {
		t.Fatal("IPv4 benchmark packet is invalid")
	}
	if !ValidatePacket(ipv6Packet) {
		t.Fatal("IPv6 benchmark packet is invalid")
	}
}
